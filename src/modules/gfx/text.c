/*
 *    Creates a text texture.
 *
 *    @param  const s8 *text    The text to render.
 *
 *    @return image_t *     The handle of the text texture.
 */
image_t *text_create(const s8 *text) {
    char          c;
    unsigned long i;
    unsigned long j;
    unsigned long k;
    unsigned long len;
    image_t      *font;
    trap_t        trap;

    if (text == (const s8 *)0x0) {
        LOGF_ERR("Invalid text.\n");
        return (image_t *)0x0;
    }

    len  = strlen(text);
    font = image_create(8 * len, 13, 69);

    if (font == (image_t *)0x0) {
        LOGF_ERR("Failed to create font image.\n");
        return (image_t *)0x0;
    }

    for (i = 0; i < len; i++) {
        for (j = 0; j < 13; j++) {
            for (k = 0; k < 8; k++) {
                c = text[i] - 32;

                /*
                 *    If the character is invalid, use the space character.
                 */
                c = MIN(c, 95);
                c = MAX(c, 0);
                if (_font[c][j] & (1 << k)) {
                    image_set_pixel(font, i * 8 + 7 - k, j, 0xffffffff);
                } else {
                    image_set_pixel(font, i * 8 + 7 - k, j, 0x00000000);
                }
            }
        }
    }

    return font;
}